
public class App {

	public static void main(String[] args) {
		//int stand for integer that holds a whole numbers. could be positive or negative.		
		int aValue = 42;
		short aShort = 93;
		long aLong = 8304;
		//double is use for precise decimal number		
		double aDouble = 3.4553;
		//float is short version of double and must use f at the end		
		float aFloat = 5.234f;
		//char holds single character inside quote		
		char aChar = 'u';
		//boolean holds true or false
		boolean aBoolean = true;
		//holds only 6 byte of data. can hold positive or negative numbers but it can hold only up to +127 and -128		
		byte aByte = 127;
		
		System.out.println(aValue);
		System.out.println(aShort);
		System.out.println(aLong);
		System.out.println(aDouble);
		System.out.println(aFloat);
		System.out.println(aChar);
		System.out.println(aBoolean);
		System.out.println(aByte);
	}

}
